package com.run.entity;

public class BookChapter {
	private int id;
	private String chapter;
	
	public void setId(int id) {
		this.id = id;
	}
	
	public int getId() {
		return id;
	}
	
	public void setChapter(String chapter) {
		this.chapter = chapter;
	}
	
	public String getChapter() {
		return chapter;
	}
}
