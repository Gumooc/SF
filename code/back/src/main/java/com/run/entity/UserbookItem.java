package com.run.entity;

public class UserbookItem {
	private int uid;
	private int bid;
	private String lst;
	
	public void setUid(int uid) {
		this.uid = uid;
	}
	
	public int getUid() {
		return uid;
	}
	
	public void setBid(int bid) {
		this.bid = bid;
	}
	
	public int getBid() {
		return bid;
	}
	
	public void setLst(String lst) {
		this.lst = lst;
	}
	
	public String getLst() {
		return lst;
	}
}
